#define CHECK_RET(x, msg) do {                                      \
    esp_err_t __err_rc = (x);                                       \
    if (__err_rc != ESP_OK) {                                       \
        ESP_LOGE(TAG, "%s failed: %s", msg, esp_err_to_name(__err_rc)); \
        return __err_rc;                                            \
    } else {                                                        \
        ESP_LOGI(TAG, "%s OK", msg);                                \
    }                                                               \
} while (0)

